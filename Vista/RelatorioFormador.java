    public void listarRelatorio() {

        Distrito d = null;
        if (ComboBoxpesquisadistrito.getSelectedIndex() != 0) {
            d = (Distrito) ComboBoxpesquisadistrito.getSelectedItem();
        }
        Localidade l = null;
        if (ComboBoxpesquisalocalidade.getSelectedIndex() != 0) {
            l = (Localidade) ComboBoxpesquisalocalidade.getSelectedItem();
        }
        //  List<Relestadociv> le = new RelatorioCtr().getREstadocivil(d, null, l);
        List<Relformador> formadores = new RelatorioFormandoCTR().getRNrformador(d, null, l);

        DefaultTableModel tbm = (DefaultTableModel) listadeformador.getModel();

        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        int i = 0;
        for (Relformador f : formadores) {
            tbm.addRow(new String[1]);

            listadeformador.setValueAt(f.getDistrito(), i, 0);
            listadeformador.setValueAt(f.getPosto(), i, 1);
            listadeformador.setValueAt(f.getLocalidade(), i, 2);
//                     listadeformando.setValueAt(f.getSexo(), i, 2);
            listadeformador.setValueAt(f.getQhomen(), i, 3);
            listadeformador.setValueAt(f.getQmulher(), i, 4);
            listadeformador.setValueAt(f.getTotal(), i, 5);

            i++;
        }

    }

