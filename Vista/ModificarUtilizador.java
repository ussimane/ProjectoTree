     private void prenchercomboPrevilegio() throws SQLException {
       
             List<Previlegio> prev = new PrevilegioJpaController().findPrevilegioEntities();
                comboprevilegio.removeAllItems();
                for ( Previlegio d : prev) {
                        comboprevilegio.addItem(d.getPrevilegio());
               }
           
    }

    private void cadastrarCurso() {
    try {
        Utilizador ut=new UtilizadorJpaController().findUtilizador(u.getIdutilizador());
       int id=ut.getIdutilizador();
        ut.setIdutilizador(id);
        ut.setNome(txtNome.getText());
        ut.setUtilizador(txtUtilizador.getText());
        ut.setSenha(txtSenha.getText());
        ut.setIdprevilegio(new Previlegio(comboprevilegio.getSelectedIndex()+1));
        new UtilizadorJpaController().edit(ut);
        JOptionPane.showMessageDialog(null, "Modificado com sucesso");
        dispose();
    } catch (NonexistentEntityException ex) {
        Logger.getLogger(ModificarUtilizador.class.getName()).log(Level.SEVERE, null, ex);
    } catch (Exception ex) {
        Logger.getLogger(ModificarUtilizador.class.getName()).log(Level.SEVERE, null, ex);
    }
    }
    private void prenchercampos(){
    Utilizador ut=new UtilizadorJpaController().findUtilizador(u.getIdutilizador());
    txtNome.setText(ut.getNome());
    txtUtilizador.setText(ut.getUtilizador());
    comboprevilegio.setSelectedItem(ut.getIdprevilegio().getPrevilegio());
    txtSenha.setText(ut.getSenha());
    
    
    }
