        private void adicionarcursos(){
            Actividadecontapropria acpo=new ActividadecontapropriaJpaController().findActividadecontapropria(idformando);
          
           

                 if(simContaPropria.isSelected()){
                     try {
                         Actividadecontapropria acp=new Actividadecontapropria();
                         
                         acp.setIdformando(idformando);
                       // JOptionPane.showMessageDialog(null, acp.getIdformando());
                         acp.setAreaactividade(txtAreaActividadeContaPropria.getText());
                         
                         if(!txtTempocontapropria.getText().trim().equals("")){
                             acp.setTempo(Integer.valueOf(txtTempocontapropria.getText()));
                             
                         }
                          else
                              acp.setTempo(0);
                         if(!txtrendimentocontapropria.getText().trim().equals("")){
                              acp.setRendimento(Float.valueOf(txtrendimentocontapropria.getText()));
                         } else
                             acp.setRendimento(Float.valueOf(0));
                         if(Fora.isSelected()){
                            acp.setForacomunidade(true);
                            acp.setLugar(txtlugarContapropria.getText());
            
                         } 
                         else 
                             if(comunidade.isSelected()){
                                 acp.setForacomunidade(false);
                        }
                         if(acpo!=null){
                            new ActividadecontapropriaJpaController().edit(acp);
                         }else
                         {
                             new ActividadecontapropriaJpaController().edit(acp);
                         }   
                            
                     } catch (IllegalOrphanException ex) {   
                         Logger.getLogger(ModificarFormando.class.getName()).log(Level.SEVERE, null, ex);
                     } catch (NonexistentEntityException ex) {
                         Logger.getLogger(ModificarFormando.class.getName()).log(Level.SEVERE, null, ex);
                     } catch (Exception ex) {
                         Logger.getLogger(ModificarFormando.class.getName()).log(Level.SEVERE, null, ex);
                     }
                    
                            } else{
                                     limparcampos();
                                  }
                        
                         
                         
                                      limparcampos();
                   
           
        
        }
        
         
        
