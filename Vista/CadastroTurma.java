    private void verificarcampos() {
        if (idEmp == 0) {
            // txtnome.requestFocus();
            JOptionPane.showMessageDialog(null, "Não foi selecionado o formando", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (dinicio.getDate().equals("")) {
            dinicio.requestFocus();
            JOptionPane.showMessageDialog(null, "Data do inicio das aulas", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (dfim.getDate().equals("")) {
            dfim.requestFocus();
            JOptionPane.showMessageDialog(null, "Data do fim das aulas", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (txthorasfreq1.getText().trim().equals("")) {
            txthorasfreq1.requestFocus();
            JOptionPane.showMessageDialog(null, "Horas frequentadas no primeiro mes", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (txthorasfalta1.getText().trim().equals("")) {
            txthorasfalta1.requestFocus();
            JOptionPane.showMessageDialog(null, "Horas faltas no primeiro mes", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
//         if (v<6) {
//            
//            JOptionPane.showMessageDialog(null, "Inseriu valor da classificação acima de previsto", "Aviso", JOptionPane.WARNING_MESSAGE);
//            return;
//        }
        cadastrarAulas();
        cadastrarprodutos();
        limparcampos();
    }

