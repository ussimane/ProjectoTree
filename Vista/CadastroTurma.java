    public void listarFormando() {
        if (combocurso.getSelectedItem() != null && ComboLocalidade.getSelectedItem() != null) {
            int l = 0;
            int c = 0;
            l = (((Localidade) ComboLocalidade.getSelectedItem()).getIdlocalidade());
            c = (((Curso) combocurso.getSelectedItem()).getIdcurso());

            List<Formando> formandos = new FormandoJpaController().getprio1(l, c);

            DefaultTableModel tbm = (DefaultTableModel) listaformando.getModel();

            for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
                tbm.removeRow(i);
            }
            int i = 0;
            for (Formando f : formandos) {
                tbm.addRow(new String[1]);

                listaformando.setValueAt(f.getIdformando(), i, 0);
                listaformando.setValueAt(f.getNome(), i, 1);



                i++;
            }
        }
    }

